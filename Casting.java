public class Casting
  
{
    public static void main(String[]args)
    {
        byte b=10;
        short s=b;
        int a=s;
        long l=b;
        float f=l;
        System.out.println("b" +b);
        System.out.println("a" +a);
        System.out.println("S" +s);
        System.out.println("l" +l);
        System.out.println("f" +f);
    }
}
    
